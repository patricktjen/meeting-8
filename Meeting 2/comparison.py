a = 10
b = 5

print(a > b)
print(a >= b)
print(a < b)
print(a <= b)
print(a == b)
print(a != b)

benar = True
salah = False

print("AND Truth Table")
print(salah and salah)
print(salah and benar)
print(benar and salah)
print(benar and benar)

print("OR Truth Table")
print(salah or salah)
print(salah or benar)
print(benar or salah)
print(benar or benar)

print("CAMPURAN Truth Table")
print(salah or salah or salah or benar)
